public class Wrapper {
    
    
    public static void main(String[] args) {
   	 int i=10;
 	float f=16f;
     boolean b=true;
     double d=45;
     char c='r';
     byte bb=23;
     long l=354;
     short s=45;

       //object
     
		Integer ii=new Integer(i);
		Float ff=new Float(f);
		Boolean bbb=new Boolean(b);
		Double dd=new Double(d);
		Character cc=new Character(c);
		Byte ab=new Byte(bb);
		Long ll=new Long(l);
		Short ss=new Short(s);
		
		//autoboxing
		
		Integer in=i;
		Float fl=f;
		Boolean bl=b;
		Double so=d;
		Character cha=c;
		Byte te=bb;
		Long lon=l;
		Short sha=s;
		
		//unboxing
		
		int ix=in;
		byte y =bb;
		short t=sha;
		long o=l;
		float lf=f;
		double u=d;
		char r=c;
		boolean e=b;
		

		
		
		
	}
}
    
